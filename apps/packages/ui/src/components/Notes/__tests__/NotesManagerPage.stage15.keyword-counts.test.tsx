const renderPage = () => {
  const queryClient = new QueryClient({
    defaultOptions: {
      queries: { retry: false },
      mutations: { retry: false }
    }
  })
  return render(
    <QueryClientProvider client={queryClient}>
      <NotesManagerPage />
    </QueryClientProvider>
  )
}

describe("NotesManagerPage stage 15 keyword counts", () => {
  beforeEach(() => {
    vi.clearAllMocks()
    mockConfirmDanger.mockResolvedValue(true)
    mockGetSetting.mockResolvedValue(null)
    mockSetSetting.mockResolvedValue(undefined)
    mockClearSetting.mockResolvedValue(undefined)
    mockGetAllNoteKeywordStats.mockResolvedValue([
      { keyword: "research", noteCount: 12 },
      { keyword: "ml", noteCount: 0 }
    ])
    mockSearchNoteKeywords.mockResolvedValue(["research", "ml"])
    mockBgRequest.mockImplementation(async (request: { path?: string; method?: string }) => {
      const path = String(request.path || "")
      const method = String(request.method || "GET").toUpperCase()
      if (path.startsWith("/api/v1/notes/?")) {
        return { items: [], pagination: { total_items: 0, total_pages: 1 } }
      }
      if (path === "/api/v1/admin/notes/title-settings" && method === "GET") {
        return {
          llm_enabled: false,
          default_strategy: "heuristic",
          effective_strategy: "heuristic",
          strategies: ["heuristic", "llm", "llm_fallback"]
        }
      }
      return {}
    })
  })

  it("shows keyword note counts in both filter dropdown and browse-keywords modal", async () => {
    renderPage()
    await waitFor(() => {
      expect(mockGetAllNoteKeywordStats).toHaveBeenCalled()
    })

    fireEvent.mouseDown(screen.getByText("Filter by keyword"))
    await waitFor(() => {
      expect(screen.getByText("research (12)")).toBeInTheDocument()
      expect(screen.getByText("ml (0)")).toBeInTheDocument()
    })

    fireEvent.click(screen.getByRole("button", { name: "Browse keywords" }))
    await waitFor(() => {
      expect(screen.getByText("Apply filters")).toBeInTheDocument()
    })
    expect(screen.getAllByText("research (12)").length).toBeGreaterThan(0)
    expect(screen.getAllByText("ml (0)").length).toBeGreaterThan(0)
  })
})
