const renderPage = () => {
  const queryClient = new QueryClient({
    defaultOptions: {
      queries: { retry: false },
      mutations: { retry: false }
    }
  })
  return render(
    <QueryClientProvider client={queryClient}>
      <NotesManagerPage />
    </QueryClientProvider>
  )
}

describe("NotesManagerPage stage 17 scalability hint", () => {
  beforeEach(() => {
    vi.clearAllMocks()
    mockConfirmDanger.mockResolvedValue(true)
    mockGetSetting.mockResolvedValue(null)
    mockSetSetting.mockResolvedValue(undefined)
    mockClearSetting.mockResolvedValue(undefined)

    const pageItems = Array.from({ length: 20 }, (_, index) => ({
      id: `n-${index + 1}`,
      title: `Note ${index + 1}`,
      content: `content ${index + 1}`,
      version: 1
    }))

    mockBgRequest.mockImplementation(async (request: { path?: string; method?: string }) => {
      const path = String(request.path || "")
      const method = String(request.method || "GET").toUpperCase()
      if (path.startsWith("/api/v1/notes/?")) {
        return {
          items: pageItems,
          pagination: { total_items: 550, total_pages: 28 }
        }
      }
      if (path === "/api/v1/admin/notes/title-settings" && method === "GET") {
        return {
          llm_enabled: false,
          default_strategy: "heuristic",
          effective_strategy: "heuristic",
          strategies: ["heuristic", "llm", "llm_fallback"]
        }
      }
      return {}
    })
  })

  it("shows large-list pagination hint and preserves bulk-selection behavior", async () => {
    renderPage()

    await waitFor(() => {
      expect(screen.getByTestId("notes-large-list-pagination-hint")).toBeInTheDocument()
    })

    fireEvent.click(screen.getByTestId("mock-select-first-note"))
    await waitFor(() => {
      expect(screen.getByTestId("notes-bulk-actions-bar")).toBeInTheDocument()
    })
  })
})
